package com.css.stringserviceprovider;


public class StringServiceProvider {
	 
	
	public static void main(String args[])  
	{  
	String s="WELCOME";   
                   
	System.out.print("After reverse string is: ");  
	for(int i=s.length();i>0;--i)                  
	{  
	System.out.print(s.charAt(i-1));            
	}  
	}  

}
